# 统一把变量名映射成中文名称和中文含义，供筛选分析和结果落地表复用。
def build_feature_display_frame(feature_names: list[str]) -> pd.DataFrame:
    """把变量名映射成中文名称和中文含义。"""
    rows: list[dict[str, str]] = []
    for feature_name in feature_names:
        if feature_name not in FEATURE_DISPLAY_META:
            raise KeyError(f"未配置变量中文说明：{feature_name}")
        chinese_name, chinese_meaning = FEATURE_DISPLAY_META[feature_name]
        rows.append(
            {
                "feature_name": feature_name,
                "feature_chinese_name": chinese_name,
                "feature_chinese_meaning": chinese_meaning,
            }
        )
    return pd.DataFrame(rows)
